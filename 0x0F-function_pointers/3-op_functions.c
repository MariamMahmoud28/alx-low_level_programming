#include "3-calc.h"

/**
 * op_add - adds two integers
 * @a: first int
 * @b: second int
 * Return: value
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - integers
 * @a: first int
 * @b: second int
 *
 * Return: value
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_sub - integers
 * @a: first int
 * @b: second int
 *
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_sub - integers
 * @a: first int
 * @b: second int
 *
 * Return: quotinent
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_sub - integers
 * @a: first int
 * @b: second int
 *
 * Return: value
 */
int op_mod(int a, int b)
{
	return (a % b);
}
